        // Script para mejorar las animaciones y efectos
        document.addEventListener('DOMContentLoaded', function() {
            // Animación de entrada escalonada
            const cards = document.querySelectorAll('.level-card');
            
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            };

            const observer = new IntersectionObserver((entries) => {
                entries.forEach((entry) => {
                    if (entry.isIntersecting) {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }
                });
            }, observerOptions);

            cards.forEach((card, index) => {
                card.style.opacity = '0';
                card.style.transform = 'translateY(50px)';
                card.style.transition = `opacity 0.6s ease-out ${index * 0.1}s, transform 0.6s ease-out ${index * 0.1}s`;
                observer.observe(card);
            });

            // Efecto parallax suave en el fondo
            window.addEventListener('scroll', () => {
                const scrolled = window.pageYOffset;
                const section = document.querySelector('.academic-levels-section');
                if (section) {
                    const rect = section.getBoundingClientRect();
                    if (rect.top < window.innerHeight && rect.bottom > 0) {
                        const parallaxSpeed = scrolled * 0.5;
                        section.style.backgroundPosition = `center ${parallaxSpeed}px`;
                    }
                }
            });

            // Efecto de hover mejorado para los badges
            document.querySelectorAll('.level-badge').forEach(badge => {
                badge.addEventListener('mouseenter', function() {
                    this.style.transform = 'scale(1.05) rotate(2deg)';
                });
                
                badge.addEventListener('mouseleave', function() {
                    this.style.transform = 'scale(1) rotate(0deg)';
                });
            });

            // Efecto de resplandor en los iconos del overlay
            document.querySelectorAll('.overlay-icon').forEach(icon => {
                setInterval(() => {
                    icon.style.boxShadow = '0 0 20px rgba(255, 255, 255, 0.3)';
                    setTimeout(() => {
                        icon.style.boxShadow = '0 8px 32px rgba(0,0,0,0.1)';
                    }, 1000);
                }, 3000);
            });
        });
